module Day10Spec (spec) where

import Day10 (day10a, day10b)
import Test.Hspec (Spec, describe, it, shouldBe)

spec :: Spec
spec = do
    describe "part 1" $
        it "examples" $ do
            day10a (unlines [".#..#", ".....", "#####", "....#", "...##"])
                `shouldBe` 8
            day10a (unlines
              [ "......#.#."
              , "#..#.#...."
              , "..#######."
              , ".#.#.###.."
              , ".#..#....."
              , "..#....#.#"
              , "#..#....#."
              , ".##.#..###"
              , "##...#..#."
              , ".#....####"
              ]) `shouldBe` 33
            day10a (unlines
              [ "#.#...#.#."
              , ".###....#."
              , ".#....#..."
              , "##.#.#.#.#"
              , "....#.#.#."
              , ".##..###.#"
              , "..#...##.."
              , "..##....##"
              , "......#..."
              , ".####.###."
              ]) `shouldBe` 35
            day10a (unlines
              [ ".#..#..###"
              , "####.###.#"
              , "....###.#."
              , "..###.##.#"
              , "##.##.#.#."
              , "....###..#"
              , "..#.#..#.#"
              , "#..#.#.###"
              , ".##...##.#"
              , ".....#.#.."
              ]) `shouldBe` 41
            day10a (unlines
              [ ".#..##.###...#######"
              , "##.############..##."
              , ".#.######.########.#"
              , ".###.#######.####.#."
              , "#####.##.#.##.###.##"
              , "..#####..#.#########"
              , "####################"
              , "#.####....###.#.#.##"
              , "##.#################"
              , "#####.##.###..####.."
              , "..######..##.#######"
              , "####.##.####...##..#"
              , ".#####..#.######.###"
              , "##...#.##########..."
              , "#.##########.#######"
              , ".####.#.###.###.#.##"
              , "....##.##.###..#####"
              , ".#.#.###########.###"
              , "#.#.#.#####.####.###"
              , "###.##.####.##.#..##"
              ]) `shouldBe` 210
    describe "part 2" $
        it "examples" $
            day10b (unlines
              [ ".#....#####...#.."
              , "##...##.#####..##"
              , "##...#...#.#####."
              , "..#.....#...###.."
              , "..#.#.....#....##"
              ]) `shouldBe`
              [ 801, 900, 901, 1000, 902, 1101, 1201, 1102, 1501, 1202, 1302
              , 1402, 1502, 1203, 1604, 1504, 1004, 404, 204, 203, 2, 102, 1
              , 101, 502, 100, 501, 601, 600, 700, 800, 1001, 1400, 1601, 1303
              , 1403
              ]
