package io.github.ephemient.aoc2019

import kotlin.test.Test
import kotlin.test.assertEquals

class Day10Test {
    @Test
    fun `part 1 examples`() {
        assertEquals(
            8,
            Day10(
                """
                .#..#
                .....
                #####
                ....#
                ...##
                """.trimIndent().lines()
            ).part1()
        )
        assertEquals(
            33,
            Day10(
                """
                ......#.#.
                #..#.#....
                ..#######.
                .#.#.###..
                .#..#.....
                ..#....#.#
                #..#....#.
                .##.#..###
                ##...#..#.
                .#....####
                """.trimIndent().lines()
            ).part1()
        )
        assertEquals(
            35,
            Day10(
                """
                #.#...#.#.
                .###....#.
                .#....#...
                ##.#.#.#.#
                ....#.#.#.
                .##..###.#
                ..#...##..
                ..##....##
                ......#...
                .####.###.
                """.trimIndent().lines()
            ).part1()
        )
        assertEquals(
            41,
            Day10(
                """
                .#..#..###
                ####.###.#
                ....###.#.
                ..###.##.#
                ##.##.#.#.
                ....###..#
                ..#.#..#.#
                #..#.#.###
                .##...##.#
                .....#.#..
                """.trimIndent().lines()
            ).part1()
        )
        assertEquals(
            210,
            Day10(
                """
                .#..##.###...#######
                ##.############..##.
                .#.######.########.#
                .###.#######.####.#.
                #####.##.#.##.###.##
                ..#####..#.#########
                ####################
                #.####....###.#.#.##
                ##.#################
                #####.##.###..####..
                ..######..##.#######
                ####.##.####...##..#
                .#####..#.######.###
                ##...#.##########...
                #.##########.#######
                .####.#.###.###.#.##
                ....##.##.###..#####
                .#.#.###########.###
                #.#.#.#####.####.###
                ###.##.####.##.#..##
                """.trimIndent().lines()
            ).part1()
        )
    }

    @Test
    fun `part 2 examples`() {
        assertEquals(
            203,
            Day10(
                """
                .#....#####...#..
                ##...##.#####..##
                ##...#...#.#####.
                ..#.....#...###..
                ..#.#.....#....##
                """.trimIndent().lines()
            ).part2(20)
        )
    }
}
